#include "main.h"
/**
 * main - function that prints n elements of an array of integers,
 * @a: array name
 * @n: is the number of element of array to be printed
 * Return: a and n input
 */
void print_array(int *a, int n);
{
	int i;
	for (i = 0; i < (n -1); i++);

	{
		printf("%d", a[n-i]);
	}
	printf("\n");
}
